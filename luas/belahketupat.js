let panjang = Number(prompt(`masukkan panjang d1 (cm)`))
let lebar = Number(prompt("masukkan panjang d2 (cm)"))
let jumlahPanjangLebar = panjang * lebar
const luasJajarGenjang = 1/2 * jumlahPanjangLebar
alert( `Dengan panjang d1 ${panjang}cm, panjang d2 ${lebar}cm, maka luas belah ketupat tersebut adalah ${luasJajarGenjang}cm`)