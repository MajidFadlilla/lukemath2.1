let sisia = Number(prompt(`masukkan sisi a(cm)`))
let sisib = Number(prompt(`masukkan sisi b(cm)`))
let sisic = Number(prompt(`masukkan sisi c(cm)`))

const kelilingSegitiga = sisia + sisib + sisic

alert(`dengan panjang sisi sepanjang ${sisia}cm, ${sisib}cm, ${sisic}cm, maka keliling segitiga tersebut adalah = ${kelilingSegitiga}cm`)